n = int(input())

for i in range(2*n-1):
    if i == 0 or i == 2*n-2:
        print('*' * n + ' ' * (2*(n-2)+1) + '*' * n)
    else:
        if i < n-1:
            print(' ' * i + '*' + ' ' * (n-2) + '*' + ' ' * (2*(n-i-1)-1) + '*' + ' ' * (n-2) + '*')
        elif i == n-1:
            print(' ' * (n-1) + '*' + ' ' * (n-2) + '*' + ' ' * (n-2) + '*')
        else:
            print(' ' * (2*n-i-2) + '*' + ' ' * (n-2) + '*' + ' ' * (2*(i-n+1)-1) + '*' + ' ' * (n-2) + '*')